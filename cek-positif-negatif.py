#Menginput Angka
bil = float(input("Tulis Sebuah Bilangan: "))

#Menampilkan Kondisi Jika Bilangan Positif
if bil > 0:
   print("Bilangan Positif")

#Menampilkan Kondisi Jika Bilangan Nol   
elif bil == 0:
   print("Bilangan Nol")

#Menampilkan Kondisi Jika Bilangan Negatif
else:
   print("Bilangan Negatif")